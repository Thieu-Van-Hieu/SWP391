package utils.mapper;

public class Target3 {
	private int x;
	private String y;
	private String z;

	public Target3() {
	}

	public Target3(int x, String y, String z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}

	public int getX() {
		return x;
	}

	public String getY() {
		return y;
	}

	public String getZ() {
		return z;
	}
	
	
}
