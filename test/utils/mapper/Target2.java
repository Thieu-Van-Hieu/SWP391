package utils.mapper;

public class Target2 {

    private double x;
    private String y;

    public Target2() {
    }

    public Target2(double x, String y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public String getY() {
        return y;
    }
}
